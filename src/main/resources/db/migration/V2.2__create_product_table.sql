-- Tạo GIN Index ngay sau khi tạo bảng Products để tối ưu tốc độ cho toán tử @>
CREATE INDEX IF NOT EXISTS idx_products_product_tags_gin ON products USING gin (product_tags jsonb_path_ops);

-- Bổ sung Index cho các cột thường xuyên được dùng để Lọc (Filter)
CREATE INDEX IF NOT EXISTS idx_products_category ON products (category_id);