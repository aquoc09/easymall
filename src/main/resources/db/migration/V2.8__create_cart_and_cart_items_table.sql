CREATE TABLE IF NOT EXISTS carts (
    cart_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    is_active BOOLEAN DEFAULT TRUE,
    user_id BIGINT REFERENCES users (user_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS cart_items (
    cart_item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cart_id BIGINT REFERENCES carts (cart_id) ON DELETE CASCADE,
    variant_id BIGINT REFERENCES product_variants (variant_id) ON DELETE CASCADE,
    quantity INT NOT NULL CHECK (quantity > 0),
    total_money DECIMAL(12, 2) NOT NULL,
    note VARCHAR(200) NULL
);